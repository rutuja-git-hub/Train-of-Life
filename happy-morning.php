                                <p>Take a cup of rolled oats (serve for two) and add into the sauce pan, add 2 cups of milk into it, as it gets heated add 1 tablespoon of chia seeds ,1/2 tablespoon vanilla essence, 1 full cut banana as it will add natural sweetness to the dish, 1/2 tablespoon cinnamon powder for an amazing flavor. If you want it to be more sweet , you can add a tablespoon of sugar or jaggery powder(I suggest you to go for the later as it is healthier and also tastes natural). Let it all cook on a medium heat for 10 mins! After that serve your delicious oatmeal with the toppings of crunchy nuts and juicy berries! I know right! Yes, it's quick and easy and yummmmmmmmmmmmmmmmmmmmmyyyyyy. So what are you waiting for, go enjoy your hot and delicious oatmeal!</p>
                                  
                             
