                                <p>Sometimes I become a lazy bum when it comes to preparing dinner,after a long day you just wanna lie down and chill,but then your tummy won't let you sleep peacefully if it's left empty! So I'll be sharing a quick and easy recipe for dinner , but before sharing it let me give a disclaimer first . (This is not a typical dinner dish, it's a kinda mix and match of an item eaten in breakfast and usually eaten as a side dish in lunch, In India mostly). </p><p>OK then! let's begin...</p><p> You need: </p><ul ><li>Brown bread, </li>
                                <li> Olive Oil,</li>
                                <li> Ripe Avocado,</li>
                                <li>  Fresh Lettuce,</li>
                                <li> Plain yogurt,</li>
                                <li>Beetroot,</li>
                                <li> Carrot,</li>
                                <li>Black Pepper,</li>
                                <li>Cucumber,</li>
                                <li>Lemon,</li>
                                <li>Chilly Powder,</li>
                                <li>Oregano, </li>
                                 <li>Asafoedita(Hing),  </li>
                                 <li>Salt</li>
                                </ul>  
                                <p>1st on our menu is Avocado Toast with Crunchy lettuce on top- Take the brown bread as per the requirements (I took two cause yeh I'm a Gannet!) , Spread some olive oil on both the sides of the bread and put it in the toaster ,(well to obviously toast or why else would you put it there) You can also toast the breads on the pan if you don't have the toaster , No worries pal ,It'll still taste great. Simultaneously take a ripe avocado and smash it with the help of a fork ,after smashing it squash half a lemon, 1/2 tablespoon of salt, 1/2 tablespoon of chilly powder , sprinkle some oregano like magic on it and mix the whole thing in the bowl, put the bowl aside and now bring on some green crunchy lettuce ,toss it in the pan and sprinkle some olive oil and a pinch of salt on it , toss it for 2 mins on the medium heat in the pan on the
gas. Now take the toasts spread the avocado mixture on it and top it with the crunchy lettuce! 2nd on our menu is Carrot-Beetroot-Cucumber Salad- Grate the Carrot,Cucumber and Beetroot ( I took 1 whole carrot, cucumber and beetroot this is to serve 1), Put the grated veggies in a bowl of yogurt (yogurt is up to 400g). Add 1/2 tablespoon of black pepper,1/2 tablespoon of salt and mix the whole thing. Now comes the best part TADKA! i.e Tempering, heat a tablespoon of olive oil and add 1/2 a tablespoon of some cumin and asafoedita (hing). Add this tadka in the yogurt and veggies mixture and your salad is readddaaayyyy(ready) . My heart dances when I see rainbow in my food plate !!!! :D
Enjoy your super comfy meal my dear friends! :)</p>
                              
                              
